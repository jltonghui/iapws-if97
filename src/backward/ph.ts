// ─── Backward Polynomial Evaluator ─────────────────────────────────────────

function evalPoly(table: CoefficientTable, piShift: number, etaShift: number, pi: number, eta: number): number {
  let sum = 0;
  for (const [I, J, N] of table) {
    sum += N * Math.pow(pi - piShift, I) * Math.pow(eta - etaShift, J);
  }
  return sum;
}

