function r2BackwardT(p: number, h: number): number {
  let T: number;
  if (p <= C.R2_P_CRT) {
    T = r2aBackwardT(p, h);
  } else if (p <= 6.546699678) {
    T = r2bBackwardT(p, h);
  } else {
    if (p < 905.84278514723 - 0.67955786399241 * h + 1.2809002730136e-4 * h * h) {
      T = r2bBackwardT(p, h);
    } else {
      T = r2cBackwardT(p, h);
    }
  }
  if (p <= C.Pc) {
    T = Math.max(saturationTemperature(p), T);
  }
  return T;
}

