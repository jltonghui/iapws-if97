function r2BackwardT(p: number, s: number): number {
  if (p <= C.R2_P_CRT) {
    return evalPoly(R2A_PS, 0, 2, p, s / 2);
  }
  if (s >= C.R2_S_CRT) {
    return evalPoly(R2B_PS, 0, 10, p, s / 0.7853);
  }
  return evalPoly(R2C_PS, 0, 2, p, s / 2.9251);
}

