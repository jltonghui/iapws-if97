function evalPoly(table: CoefficientTable, piShift: number, sigShift: number, pi: number, sig: number): number {
  let sum = 0;
  for (const [I, J, N] of table) sum += N * Math.pow(pi - piShift, I) * Math.pow(sig - sigShift, J);
  return sum;
}

