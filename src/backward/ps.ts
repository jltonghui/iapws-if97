// ─── Main PS Solver ────────────────────────────────────────────────────────

/**
 * Solve for thermodynamic state given P and S.
 * @param p - Pressure [MPa]
 * @param s - Specific entropy [kJ/(kg·K)]
 */
export function solvePS(p: number, s: number): BasicProperties {
  if (p < C.P_MIN || p > C.P_MAX) {
    throw new OutOfRangeError('Pressure', p, C.P_MIN, C.P_MAX);
  }
  if (s < C.S_MIN || s > C.S_MAX) {
    throw new OutOfRangeError('Entropy', s, C.S_MIN, C.S_MAX);
  }
  assertCriticalRegion4PSInput(p, s, 'solvePS');

  const region = detectRegionPS(p, s);

  switch (region) {
    case Region.Region1: {
      const T0 = r1BackwardT(p, s);
      const T = newtonRaphson((T_x) => region1(p, T_x).entropy - s, T0);
      return validateBackwardState(
        region1(p, T),
        [
          { label: 'pressure', expected: p },
          { label: 'entropy', expected: s },
        ],
        { solverName: 'solvePS', expectedRegion: Region.Region1 },
      );
    }
    case Region.Region2: {
      const T0 = r2BackwardT(p, s);
      const T = newtonRaphson((T_x) => region2(p, T_x).entropy - s, T0);
      return validateBackwardState(
        region2(p, T),
        [
          { label: 'pressure', expected: p },
          { label: 'entropy', expected: s },
        ],
        { solverName: 'solvePS', expectedRegion: Region.Region2 },
      );
    }
    case Region.Region3: {
      let T0: number, v0: number;
      if (s <= C.R3_S_CRT) {
        T0 = 760 * evalPoly(R3A_PS_T_TABLE, -0.240, 0.703, p / 100, s / 4.4);
        v0 = 0.0028 * evalPoly(R3A_PS_V_TABLE, -0.187, 0.755, p / 100, s / 4.4);
      } else {
        T0 = 860 * evalPoly(R3B_PS_T_TABLE, -0.760, 0.818, p / 100, s / 5.3);
        v0 = 0.0088 * evalPoly(R3B_PS_V_TABLE, -0.298, 0.816, p / 100, s / 5.3);
      }
      const sol = nelderMead(
        (pair) => Math.abs(region3ByRhoT(1 / pair[0], pair[1]).entropy - s) +
                  Math.abs(region3ByRhoT(1 / pair[0], pair[1]).pressure - p),
        [v0, T0],
      );
      return validateBackwardState(
        region3ByRhoT(1 / sol.x[0], sol.x[1]),
        [
          { label: 'pressure', expected: p, tolerance: 1e-5 * Math.max(1, Math.abs(p)) },
          { label: 'entropy', expected: s },
        ],
        { solverName: 'solvePS', expectedRegion: Region.Region3 },
      );
    }
    case Region.Region4: {
      const endpoints = saturationEndpointsAtPressure(p);
      assertRegion4StateAllowed(endpoints.pressure, endpoints.temperature, 'solvePS');
      const x = qualityFromSaturationProperty(
        endpoints.liquid.entropy,
        endpoints.vapor.entropy,
        s,
      );
      return validateBackwardState(
        mixSaturationState(endpoints, x),
        [
          { label: 'pressure', expected: p },
          { label: 'entropy', expected: s },
        ],
        { solverName: 'solvePS', expectedRegion: Region.Region4 },
      );
    }
    case Region.Region5: {
      const T = newtonRaphson((T_x) => region5(p, T_x).entropy - s, 1500);
      return validateBackwardState(
        region5(p, T),
        [
          { label: 'pressure', expected: p },
          { label: 'entropy', expected: s },
        ],
        { solverName: 'solvePS', expectedRegion: Region.Region5 },
      );
    }
    default:
      throw new IF97Error(`Cannot determine region for P=${p} MPa, s=${s} kJ/(kg·K)`);
  }
}
